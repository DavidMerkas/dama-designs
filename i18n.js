const I18N_LANG_KEY = "dama-lang";

function i18nApply(lang){
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    if(!el.dataset.i18nOrig) el.dataset.i18nOrig = el.textContent.trim();
    const src = el.dataset.i18nOrig;
    el.textContent = lang === 'en' ? (I18N_EN[src] || src) : src;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el=>{
    if(!el.dataset.i18nOrigPh) el.dataset.i18nOrigPh = el.getAttribute('placeholder') || '';
    const src = el.dataset.i18nOrigPh;
    el.setAttribute('placeholder', lang === 'en' ? (I18N_EN[src] || src) : src);
  });
  document.querySelectorAll('.lang a').forEach(a=>{
    a.classList.toggle('act', a.dataset.lang === lang);
  });
  localStorage.setItem(I18N_LANG_KEY, lang);
}

document.addEventListener('DOMContentLoaded', ()=>{
  const saved = localStorage.getItem(I18N_LANG_KEY) || 'hr';
  i18nApply(saved);
  document.querySelectorAll('.lang a[data-lang]').forEach(a=>{
    a.addEventListener('click', e=>{
      e.preventDefault();
      i18nApply(a.dataset.lang);
    });
  });
});
